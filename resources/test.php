Подвеска<O:P></O:P></SPAN></P>
<P class=MsoNormal 
style="TEXT-ALIGN: justify; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt"><SPAN 
style="FONT-SIZE: 14pt"><O:P></P>
<P class=MsoNormal 
style="TEXT-ALIGN: justify; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt"><B 
style="mso-bidi-font-weight: normal"><SPAN style="FONT-SIZE: 14pt">Стальные 
балансиры</SPAN></B><SPAN style="FONT-SIZE: 14pt"> (рис.7) свободно качаются в 
