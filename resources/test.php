align=center><SPAN style="FONT-SIZE: 12pt; mso-bidi-font-size: 14.0pt">Рис.3. 
Ведущее колесо: </SPAN></P>
<P class=MsoNormal 
style="TEXT-ALIGN: center; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt" 
align=center><SPAN style="FONT-SIZE: 12pt; mso-bidi-font-size: 14.0pt">1 – 
венец; 2 – ступица; 3 – болт с гайкой<O:P></O:P></SPAN></P>
<P class=MsoNormal 
style="TEXT-ALIGN: justify; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt"><SPAN 
style="FONT-SIZE: 14pt"><O:P></O:P></SPAN></P>
<P class=MsoNormal 
style="TEXT-ALIGN: justify; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt"><SPAN 
style="FONT-SIZE: 14pt">Ведущее колесо состоит из двух зубчатых венцов и ступицы 
с приваренными к ней двумя дисками. Венцы ведущего колеса крепятся к дискам 
болтами<SPAN style="mso-spacerun: yes">&nbsp; </SPAN>с гайками и 
шплинтуются.<O:P></O:P></SPAN></P>
<P class=MsoNormal 
style="TEXT-ALIGN: justify; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt"><B 
style="mso-bidi-font-weight: normal"><SPAN style="FONT-SIZE: 14pt">Направляющие 
колеса с натяжными устройствами</SPAN></B><SPAN style="FONT-SIZE: 14pt"> (рис.4) 
предназначены обеспечить требуемое натяжение гусениц и направление их во время 
движения транспортера. Они расположены в кормовой части 
транспортера.<O:P></O:P></SPAN></P>
<P class=MsoNormal 
style="TEXT-ALIGN: justify; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt"><SPAN 
