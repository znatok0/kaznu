торсионных валов и одни правый (шестой), с правой — пять правых и один левый 
(шестой) (рис 7).<O:P></O:P></SPAN></P>
<P class=MsoNormal 
style="TEXT-ALIGN: justify; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt"><B 
