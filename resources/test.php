<P class=MsoNormal 
style="TEXT-ALIGN: center; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt" 
align=center><SPAN style="FONT-SIZE: 12pt; mso-bidi-font-size: 14.0pt">Рис.1. 
Многоцелевой тягач - легкий, бронированный (МТ-ЛБ)</SPAN></P><SPAN 
style="FONT-SIZE: 12pt; mso-bidi-font-size: 14.0pt">
<P class=MsoNormal 
