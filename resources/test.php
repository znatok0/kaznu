style="FONT-SIZE: 14pt"><SPAN style="FONT-SIZE: 14pt"><SPAN 
style="FONT-SIZE: 14pt">Натяжной винт закреплен в шаровой опоре, установленной в 
кронштейне, вваренном в днище 
корпуса.<O:P></O:P></SPAN></SPAN></SPAN></P></SPAN></SPAN></SPAN></SPAN></SPAN></SPAN></SPAN></SPAN></SPAN></SPAN></SPAN></SPAN>
<P class=MsoNormal 
style="TEXT-ALIGN: justify; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt"><SPAN 
style="FONT-SIZE: 14pt">Натяжной винт фиксируется стопорной планкой, которая 
крепится к кронштейну шаровой опоры болтом.<O:P></O:P></SPAN></P>
<P class=MsoNormal 
style="TEXT-ALIGN: justify; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt"><B 
