<P class=MsoNormal 
style="TEXT-ALIGN: justify; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt"><B 
style="mso-bidi-font-weight: normal"><SPAN style="FONT-SIZE: 14pt">Гусеницы 
</SPAN></B><SPAN style="FONT-SIZE: 14pt">с за&shy;крытым шарниром (рис 2). 
Каждая новая гусеница состоит из 108 звеньев. Звено представляет собой фасонную 
штамповку. Звенья пятипроушинные, гребневые. Звенья соединяются в гусеницу 
обрезиненными пальцами. Резиновые кольца пальцев служат для уплотнения шарниров 
от воздействия внешней среды, что улучшает ходовые качества гусеничных 
цепей.<O:P></O:P></SPAN></P></SPAN>
<P class=MsoNormal 
style="TEXT-ALIGN: center; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt" 
