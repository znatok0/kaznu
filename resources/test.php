балансира на двух шарикоподшипниках.<O:P></O:P></SPAN></P>
<P class=MsoNormal 
style="TEXT-ALIGN: justify; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt"><SPAN 
style="FONT-SIZE: 14pt">Опорные катки выполнены из алюминиевого сплава 
пустотелыми. Опорный каток состоит из ступицы, дисков и обода. Все детали 
соединены между собой сваркой. К ободу привулканизирована массивная 
шина.<O:P></O:P></SPAN></P>
<P class=MsoNormal 
style="TEXT-ALIGN: justify; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt"><SPAN 
style="FONT-SIZE: 14pt">В колпаке выполнено отверстие, закрытое пробкой, для 
контроля уровня масла и отверстие, закрытое пробкой, для заправки и слива 
масла.<SPAN style="FONT-SIZE: 14pt"><O:P>&nbsp;</O:P></SPAN></SPAN></P>
<P class=MsoNormal 
