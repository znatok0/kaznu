				<h2 class="title">18. Ходовая часть и электрооборудование МТ-ЛБ</h2>
				<div style="clear: both;">&nbsp;</div>
				<div class="entry">
<P class=MsoNormal 
style="TEXT-ALIGN: center; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt" 
align=center><B style="mso-bidi-font-weight: normal"><SPAN 
style="FONT-SIZE: 14pt">1. Назначение и устройство гусеничного движетеля, 
подвески и корпуса<O:P></O:P></SPAN></B></P>
<P class=MsoNormal 
style="TEXT-ALIGN: justify; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt"><SPAN 
style="FONT-SIZE: 14pt"><O:P></O:P></SPAN></P>
<P class=MsoNormal 
style="TEXT-ALIGN: justify; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt"><B 
style="mso-bidi-font-weight: normal"><SPAN style="FONT-SIZE: 14pt">Ходовая часть 
транспортера состоит</SPAN></B><SPAN style="FONT-SIZE: 14pt"> из гусеничного 
движителя и подвески.<O:P></O:P></SPAN></P>
<P class=MsoNormal 
style="TEXT-ALIGN: justify; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt"><B 
style="mso-bidi-font-weight: normal"><SPAN style="FONT-SIZE: 14pt">Гусеничный 
движитель</SPAN></B><SPAN style="FONT-SIZE: 14pt"> служит для преобразования 
вращательного движения ведущих колес в поступательное движение 
транспортера.<O:P></O:P></SPAN></P>
<P class=MsoNormal 
style="TEXT-ALIGN: justify; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt"><SPAN 
style="FONT-SIZE: 14pt">Гусеничный движитель<B 
style="mso-bidi-font-weight: normal"> состоит</B> (рис.1) из двух гусениц, двух 
ведущих колес, двух направляющих колес с натяжными устройствами и двенадцати 
опорных катков.<BR style="mso-ignore: vglayout" clear=all></SPAN></P>
<P class=MsoNormal 
style="TEXT-ALIGN: center; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt" 
