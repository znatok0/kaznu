<P class=MsoNormal 
style="TEXT-ALIGN: justify; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt"><B 
style="mso-bidi-font-weight: normal"><SPAN style="FONT-SIZE: 14pt">Торсионные 
валы</SPAN></B><SPAN style="FONT-SIZE: 14pt"> (рис.7) являются упругими 
элементами подвески. Они стальные, невзаимозаменяемые. Правые торсионные валы 
окрашены в серый цвет, левые валы - в синий цвет. С левой стороны машины 
устанавливаются пять левых валов и один правый (шестой), с правой - пять правых 
и один левый (шестой).<O:P></O:P></SPAN></P>
<P class=MsoNormal 
