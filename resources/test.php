формулы называются формулами тысячной и имеют широкое применение в стрелковой 
практике. В данных формулах <B style="mso-bidi-font-weight: normal">Д</B> - 
дальность до предмета в метрах. <B style="mso-bidi-font-weight: normal">У</B> - 
угол, под которым виден предмет в тысячных. <B 
style="mso-bidi-font-weight: normal">В</B> - высота (ширина) предмета в метрах, 
т. е. длина хорды, а не дуги. При малых углах (до 15°) разница между длиной дуги 
и хорды не превышает одной тысячной, поэтому при практической работе они 
считаются равными.<O:P></O:P></SPAN></P>
<P class=MsoNormal 
