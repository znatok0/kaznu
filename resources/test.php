<P class=MsoNormal 
style="TEXT-ALIGN: justify; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt"><SPAN 
style="FONT-SIZE: 14pt"><SPAN style="FONT-SIZE: 14pt">Натяжение гусеницы 
осуществляется поворотом коленчатой оси в кронштейне, приваренном к борту 
корпуса транспортера.</SPAN></SPAN></P>
<P class=MsoNormal 
