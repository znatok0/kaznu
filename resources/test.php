<P class=MsoNormal 
style="TEXT-ALIGN: justify; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt"><B 
style="mso-bidi-font-weight: normal"><SPAN style="FONT-SIZE: 14pt">Подвеска 
</SPAN></B><SPAN style="FONT-SIZE: 14pt">(рис. 6) служит для смягчения толчков и 
ударов при движении машины. Подвеска - независимая, торсионная, состоит из 12 
балансиров, 12 торсионных валов, 4 гидроамортизаторов и 4 пружинных 
упоров.<O:P></O:P></SPAN></P>
<P class=MsoNormal 
