углов в делениях угломера (тысячных) может производиться:&nbsp;</SPAN><SPAN 
style='FONT-SIZE: 14pt; FONT-FAMILY: "Times New Roman","serif"; COLOR: black; mso-fareast-language: RU; mso-fareast-font-family: "Times New Roman"'>угломерным 
кругом буссоли, сеткой бинокля и перископа, артиллерийским кругом (на карте), 
целиком прицела, механизмом боковых поправок снайперского прицела и подручными 
предметами. Точность углового измерения с помощью того или иного прибора зависит 
от точности шкалы на нем.<O:P></O:P></SPAN></P>
