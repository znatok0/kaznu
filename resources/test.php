style="FONT-SIZE: 14pt"><O:P><IMG class=image alt=18-02.png 
src="/img/strela/18-02.png">&nbsp;</O:P></SPAN></SPAN></P>
<P class=MsoNormal 
style="TEXT-ALIGN: center; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt" 
align=center><SPAN style="FONT-SIZE: 12pt; mso-bidi-font-size: 14.0pt">Рис.2. 
Гусеница с закрытым шарниром : </SPAN></P>
<P class=MsoNormal 
style="TEXT-ALIGN: center; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt" 
align=center><SPAN style="FONT-SIZE: 12pt; mso-bidi-font-size: 14.0pt">1 – 
звено; 2 – обрезиненный палец<O:P></O:P></SPAN></P>
<P class=MsoNormal 
style="TEXT-ALIGN: justify; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt"><SPAN 
style="FONT-SIZE: 14pt"><O:P></O:P></SPAN></P>
<P class=MsoNormal 
style="TEXT-ALIGN: justify; MARGIN: 0cm 0cm 0pt; TEXT-INDENT: 35.45pt"><B 
style="mso-bidi-font-weight: normal"><SPAN style="FONT-SIZE: 14pt">Ведущие 
колеса</SPAN></B><SPAN style="FONT-SIZE: 14pt"> (рис. 3) служат для 
перематывания гусеничных лент. Колеса надеваются на шлицы хвостовика водила 
бортовой передачи.<SPAN 
style="FONT-SIZE: 14pt"><O:P>&nbsp;</O:P></SPAN></SPAN></P>
<P class=MsoNormal 
